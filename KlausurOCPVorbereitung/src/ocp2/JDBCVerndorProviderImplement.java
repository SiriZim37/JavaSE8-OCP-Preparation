package ocp2;

public class JDBCVerndorProviderImplement {

	/*
	  QUESTION 65
	 For which three objects must a vendor provide implementations in its JDBC driver? (Choose three.)
	 A. Time
	 B. Date
	 C. Statement
	 D. ResultSet
	 E. Connection
	 F. SQLException
	 G. DriverManager
	 */
	
	
	/*
	    ใน JDBC (Java Database Connectivity) ผู้ให้บริการฐานข้อมูล (vendor) ต้องทำการพัฒนาและจัดเตรียมการใช้งานบางประเภทของอ็อบเจ็กต์ในไดรเวอร์ JDBC เพื่อให้สามารถเชื่อมต่อและทำงานร่วมกับฐานข้อมูลได้อย่างถูกต้อง มาดูแต่ละตัวเลือกว่าผู้ให้บริการต้องพัฒนาและจัดเตรียมอะไรบ้าง:

		A. Time:
		ไม่ใช่การพัฒนาอ็อบเจ็กต์ที่จำเป็น. java.sql.Time คือประเภทข้อมูลที่ใช้สำหรับแสดงผลเวลาใน SQL แต่ไดรเวอร์ JDBC ไม่จำเป็นต้องพัฒนาอ็อบเจ็กต์นี้เอง โดยไดรเวอร์จะทำการแปลงข้อมูลระหว่างประเภท TIME ในฐานข้อมูลกับอ็อบเจ็กต์ Time ใน Java เท่านั้น
		B. Date:
		ไม่ใช่การพัฒนาอ็อบเจ็กต์ที่จำเป็น. java.sql.Date ใช้สำหรับแสดงวันที่ใน SQL เช่นเดียวกับ Time โดยไดรเวอร์ JDBC ทำหน้าที่แปลงข้อมูลระหว่างประเภท DATE ของฐานข้อมูลกับอ็อบเจ็กต์ Date ใน Java แต่มันไม่จำเป็นต้องพัฒนาอ็อบเจ็กต์ Date เอง
		C. Statement:
		ต้องการการพัฒนาอ็อบเจ็กต์นี้. java.sql.Statement คืออินเตอร์เฟสที่ใช้ในการส่งคำสั่ง SQL ไปยังฐานข้อมูล เช่น การคิวรีหรือการอัปเดต ข้อมูล ดังนั้นไดรเวอร์ JDBC ต้องพัฒนาและใช้งานอินเตอร์เฟสนี้เพื่อการสื่อสารกับฐานข้อมูล
		D. ResultSet:
		ต้องการการพัฒนาอ็อบเจ็กต์นี้. java.sql.ResultSet คืออินเตอร์เฟสที่ใช้เพื่อเข้าถึงผลลัพธ์จากการคิวรีข้อมูลจากฐานข้อมูล โดยไดรเวอร์ JDBC ต้องพัฒนาอินเตอร์เฟสนี้เพื่อให้สามารถใช้งานผลลัพธ์จากคำสั่ง SQL และเข้าถึงข้อมูลภายในฐานข้อมูลได้
		E. Connection:
		ต้องการการพัฒนาอ็อบเจ็กต์นี้. java.sql.Connection คืออินเตอร์เฟสที่ใช้สำหรับเชื่อมต่อกับฐานข้อมูล ไดรเวอร์ JDBC ต้องพัฒนาอินเตอร์เฟสนี้เพื่อสร้างการเชื่อมต่อ จัดการการทำธุรกรรม และทำการดำเนินการกับฐานข้อมูล
		F. SQLException:
		ไม่ใช่การพัฒนาอ็อบเจ็กต์ที่จำเป็น. SQLException เป็นคลาสที่ใช้ในการจัดการข้อผิดพลาดที่เกิดขึ้นในระหว่างการทำงานกับฐานข้อมูล ไดรเวอร์ JDBC ไม่จำเป็นต้องพัฒนาคลาสนี้ เพราะมันเป็นส่วนหนึ่งของ Java standard library ที่พร้อมใช้งานอยู่แล้ว
		G. DriverManager:
		ไม่ใช่การพัฒนาอ็อบเจ็กต์ที่จำเป็น. DriverManager เป็นคลาสที่ใช้ในการจัดการไดรเวอร์ JDBC และทำหน้าที่ในการโหลดและเชื่อมต่อกับฐานข้อมูล ไดรเวอร์ JDBC ไม่จำเป็นต้องพัฒนาอ็อบเจ็กต์นี้ เพราะ DriverManager เป็นส่วนหนึ่งของ Java standard library ที่ทำงานได้เอง
		คำตอบที่ถูกต้อง:
		ไดรเวอร์ JDBC ต้องพัฒนาอินเตอร์เฟสเหล่านี้เพื่อให้สามารถเชื่อมต่อกับฐานข้อมูลได้:
		
		C. Statement
		D. ResultSet
		E. Connection
		เหล่านี้คือลิสต์ของอ็อบเจ็กต์ที่จำเป็นต้องมีในไดรเวอร์ JDBC เพื่อให้สามารถทำงานกับฐานข้อมูลได้อย่างถูกต้อง
	 */
}
