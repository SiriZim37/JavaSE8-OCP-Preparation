package loc;

public class B01_Localization {

	public static void main(String[] args) {

		/*
		 * Internationalization (i18n): das Program so schreiben,
		 * das es sich (relativ) einfach lokalisieren lässt.
		 * 
		 * Localization (l10n): das Programm passend für ein
		 * bestimmtes Land/Sprache gestalten.
		 * 
		 *  
		 *  https://www.w3.org/International/questions/qa-i18n
		 */
		
		/*
		 * การแปล (Localization หรือ L10n) และการทำให้เป็นสากล (Internationalization หรือ i18n) 
		 * เป็นกระบวนการที่ช่วยให้ซอฟต์แวร์หรือเว็บไซต์สามารถรองรับผู้ใช้จากหลากหลายประเทศและภาษาต่างๆ ได้
		 * 
		 * 1. การทำให้เป็นสากล (Internationalization - i18n): คือการเตรียมความพร้อมให้ซอฟต์แวร์สามารถรองรับการแปล
		 * และการปรับแต่งให้เข้ากับผู้ใช้จากภูมิภาคต่างๆ โดยกระบวนการนี้จะทำในขั้นตอนการพัฒนาซอฟต์แวร์ 
		 * เช่น การใช้โครงสร้างโค้ดที่สามารถรับข้อมูลหลายภาษาได้ การแยกข้อความออกจากโค้ด และการจัดการรูปแบบวันที่ เวลา และสกุลเงินที่สามารถปรับเปลี่ยนได้
		 * 
		 * 2การแปล (Localization - L10n): คือการปรับแต่งเนื้อหาและส่วนต่างๆ ของซอฟต์แวร์
		 * ให้สอดคล้องกับภาษาและวัฒนธรรมของภูมิภาคที่เฉพาะเจาะจง โดยจะเป็นการนำซอฟต์แวร์ที่ได้ผ่านการทำให้เป็นสากลแล้วมาแปลข้อความ 
		 * ปรับสกุลเงิน เวลา วันที่ และการแสดงผลให้เหมาะสมกับวัฒนธรรมของผู้ใช้
		 */
		
		/*
		 * Lokalisierung (Localization - L10n)
		 * การแปล คือการปรับแต่งผลิตภัณฑ์ ซอฟต์แวร์ หรือเอกสารให้สอดคล้องกับภาษา
		 * วัฒนธรรม และความต้องการเฉพาะของตลาดเป้าหมายในแต่ละประเทศหรือภูมิภาค
		 * เพื่อให้ผลิตภัณฑ์ใช้งานได้ง่ายและสอดคล้องกับวิถีชีวิตของผู้ใช้ในพื้นที่นั้น ๆ
		 *
		 * การแปลไม่ได้หมายถึงการแปลข้อความในหน้าผู้ใช้หรือเอกสารเท่านั้น 
		 * แต่ยังรวมถึงการปรับแก้ไขในหลาย ๆ ด้าน เช่น:
		 * - รูปแบบของตัวเลข วันที่ และเวลา
		 * - การใช้สกุลเงิน
		 * - การจัดการแป้นพิมพ์
		 * - วิธีการเรียงลำดับและการเปรียบเทียบ
		 * - สัญลักษณ์ ตัวอักษร และสีที่อาจมีความหมายแตกต่างกันในแต่ละวัฒนธรรม
		 * - การจัดรูปแบบข้อความและภาพที่อาจต้องคำนึงถึงความเข้าใจที่ผิดพลาดในบางวัฒนธรรม
		 * - การปฏิบัติตามกฎหมายท้องถิ่นที่แตกต่างกัน
		 * ในบางกรณี การแปลอาจทำให้ต้องปรับตรรกะ รูปแบบการแสดงผล หรือตัวเนื้อหาหลัก
		 * ให้เหมาะสมกับลักษณะเฉพาะของแต่ละประเทศหรือภูมิภาค
		 */

		/*
		 * Internationalisierung (Internationalization - i18n)
		 * การทำให้เป็นสากล คือการออกแบบและพัฒนาผลิตภัณฑ์หรือแอปพลิเคชันให้รองรับการแปล
		 * หรือการปรับแต่งสำหรับกลุ่มเป้าหมายที่มีความแตกต่างกันทางภาษา วัฒนธรรม และภูมิภาค
		 * ทำให้ผลิตภัณฑ์นี้สามารถนำไปใช้ได้กับผู้ใช้ในหลากหลายวัฒนธรรมได้ง่ายขึ้นในอนาคต
		 *
		 * ขั้นตอนในการทำให้เป็นสากลประกอบด้วย:
		 * - การออกแบบซอฟต์แวร์ให้รองรับการปรับแต่งได้ง่ายและยืดหยุ่นโดยลดข้อจำกัด
		 *   ในการแปลหรือการใช้งานในระดับสากล
		 * - การสนับสนุนฟังก์ชันที่จะใช้งานได้หลังจากการแปล เช่น การเพิ่มโครงสร้างข้อความ
		 *   หรือรูปแบบ CSS ที่รองรับข้อความแนวตั้งหรือคุณสมบัติที่ไม่ใช่ตัวอักษรละติน
		 * - การตั้งค่าโค้ดให้รองรับความต้องการที่เกี่ยวข้องกับท้องถิ่น ภูมิภาค หรือวัฒนธรรม
		 *   เช่น รูปแบบของวันที่ ระบบตัวเลข และการเรียงลำดับ
		 * - แยกองค์ประกอบที่ต้องการแปลออกจากโค้ดหลัก เพื่อให้สามารถโหลดหรือเลือกใช้
		 *   ข้อมูลที่แปลแล้วได้ตามการตั้งค่าของผู้ใช้
		 *
		 * สรุป: การทำให้เป็นสากลช่วยเตรียมความพร้อมให้ซอฟต์แวร์สามารถนำไปแปลและใช้งาน
		 * ในตลาดต่าง ๆ ได้ง่ายขึ้นแม้ว่าจะยังไม่ได้แปล
		 */

		

	}

}
